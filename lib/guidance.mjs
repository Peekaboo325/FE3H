// ─────────────────────────────────────────────────────────────────────────
//  기록 지침 — 기록자(유저)가 직접 적어 본문 생성에 얹는 '전역 집필 지침'.
//
//  박제된 worldview 집필 지침은 못 건드리지만, 그 위에 유저가 실시간으로
//  얹는 개인 지침서다. 메인 본문(클로드 Opus) + 서신(말투 교정)에 주입한다.
//  요약·보고서·임무엔 안 넣는다(그 기계적 과제엔 말투 교정이 무의미). 저장은 settings 표 단일 행.
// ─────────────────────────────────────────────────────────────────────────

// 유저가 적은 지침 → system에 붙일 블록 문자열. 비어 있으면 ''(주입 안 함).
export function buildGuidanceBlock(text) {
  const t = (text || '').trim();
  if (!t) return '';
  return (
    '[기록자의 추가 집필 지침 — 이 이야기 전반에 적용]\n' +
    '아래는 기록자가 직접 남긴 지침이다. 박제된 세계관·규약과 어긋나지 않는 선에서 반드시 우선해 따른다.\n\n' +
    t
  );
}
