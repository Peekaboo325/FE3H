// 경량 맥락 — 최근 본문 1~2화만(이어지는 상황·계절). 큰 줄기·전체 요약은 본문 몫(콘티는 가볍게).
//  ⚠️ 마지막 2화만 쓰므로 꼬리만 읽는다(loadRecentTurns) — 전 회차를 끌어오지 않는다(egress 절감).
async function 경량맥락(storyId) {
  if (!storyId) return '';
  const turns = await loadRecentTurns(storyId, 8); // 8턴이면 최근 assistant 2화는 확실히 포함
  const 본문들 = turns.filter((t) => t.role === 'assistant' && t.content?.trim()).slice(-2);
  return 본문들.map((t) => t.content.trim()).join('\n\n');
}

export function buildEnrichPrompt({ prompt, context, 기록지침 = '', 회피 = '' }) {
  const 지침블록 = 기록지침 ? `\n[기록자의 지침 — 연출도 이 결을 따른다]\n${기록지침}\n` : '';
  const 회피블록문 = 회피 ? `\n${회피}\n` : '';
  return `${작성가드}
${지침블록}${회피블록문}
[최근 맥락 — 이어지는 상황·계절의 참고]
${context || '(아직 쌓인 본문이 없다 — 1차의 방향과 일반 정황으로 연출한다.)'}

[작가의 1차 방향]
${prompt}

[연출 지시문 — 다른 말 없이 지시문만]`;
}

// 연출가 = Anthropic SDK 경로(DeepSeek·Sonnet·Opus 공용). 작성가드를 system으로, 맥락+1차를 user로. 비스트리밍(콘티는 짧다).
//  DeepSeek도 Anthropic 호환 엔드포인트라 같은 코드로 돈다(서술자클라이언트/서술자키가 제공자별로 갈라 줌).
//  ⚠️ §3의 'Gemini엔 세계관 금지'는 여기 안 걸리지만, 지금은 세계관·인물을 주지 않고 '무지한 연출가'로 둔다(2순위=세계관 주입은 별도 상의). 여긴 모델만.
async function 앤스로픽콘티(model, { prompt, context, 기록지침, 회피 = '' }) {
  const client = 서술자클라이언트(model);
  if (!client || !서술자키(model)) {
    return { error: `연출가 열쇠가 없습니다(${model.startsWith('deepseek') ? 'DEEPSEEK_API_KEY' : 'ANTHROPIC_API_KEY'}).` };
  }
  const 지침블록 = 기록지침 ? `\n[기록자의 지침 — 연출도 이 결을 따른다]\n${기록지침}\n` : '';
  const user =
    (회피 ? `${회피}\n\n` : '') +
    `[최근 맥락 — 이어지는 상황·계절의 참고]\n${context || '(아직 쌓인 본문이 없다 — 1차의 방향과 일반 정황으로 연출한다.)'}\n\n` +
    `[작가의 1차 방향]\n${prompt}\n\n[연출 지시문 — 다른 말 없이 지시문만]`;
  try {
    const msg = await client.messages.create({
      model,
      max_tokens: 8000, // 헤드룸(사고 + ~500자 출력). ⚠️ DeepSeek은 thinking budget(4096)이 max_tokens 미만이어야 함 → 8000.
      ...사고옵션(model, 'low'), // Claude=adaptive+effort 낮음 / DeepSeek=고정 사고(effort 무시)
      system: [{ type: 'text', text: 작성가드 + 지침블록 }],
      messages: [{ role: 'user', content: user }],
    });
    const text = (msg.content || [])
      .filter((b) => b.type === 'text')
      .map((b) => b.text)
      .join('')
      .trim();
    await 기록사용량(model, {
      in: msg.usage?.input_tokens || 0,
      cw: msg.usage?.cache_creation_input_tokens || 0,
      cr: msg.usage?.cache_read_input_tokens || 0,
      out: msg.usage?.output_tokens || 0,
    });
    return { text };
  } catch (e) {
    console.error(`[윤색] ${model} 연출 실패:`, e?.message || e);
    return { error: '연출을 받지 못했습니다 — 다시 시도하십시오.' };
  }
}

// 윤색 실행 — 1차 프롬프트 + 경량 맥락 → 콘티(2차 산문 지시문). 연출 모델은 앱 설정에서(기본 DeepSeek).
export async function runEnrich({ storyId, prompt, model } = {}) {
  const 씨앗 = String(prompt ?? '').trim();
  if (!씨앗) return { error: '방향을 입력하십시오.' };
  const 연출 = 콘티모델(model);
  const [context, 지침, 회피] = await Promise.all([
    경량맥락(storyId),
    getGuidance(storyId).catch(() => ''),
    회피블록(storyId).catch(() => ''), // 소진 대장 → '되풀이 금지' 블록(B안)
  ]);
  const 기록지침 = buildGuidanceBlock(지침);

  let text, error;
  if (제미니콘티(연출)) {
    // Gemini(Flash) 경로 — 현재 옵션엔 없으나 재추가 대비해 남겨 둠.
    ({ text, error } = await callGemini(buildEnrichPrompt({ prompt: 씨앗, context, 기록지침, 회피 }), {
      temperature: 0.7,
      maxOutputTokens: 1000, // 콘티는 짧게(공백 포함 ~500자) — 헤드룸만
      thinkingBudget: 512,
      tag: '윤색',
    }));
  } else {
    // DeepSeek·Sonnet·Opus — Anthropic SDK 공용 경로.
    ({ text, error } = await 앤스로픽콘티(연출, { prompt: 씨앗, context, 기록지침, 회피 }));
  }
  if (error) return { error };
  const colt = String(text ?? '').trim();
  if (!colt) return { error: '연출을 받지 못했습니다 — 다시 시도하십시오.' };
  return { colt };
}
