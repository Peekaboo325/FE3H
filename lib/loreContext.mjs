// ─────────────────────────────────────────────────────────────────────────
//  활성 견문록(연재 고유 설정) → 프롬프트용 '[연재 고유 설정]' 블록 (순수 함수).
//
//  이건 이 연재가 원작에서 바꾸거나 새로 정한 '세계 설정'이다.
//  원작 기본값보다 이 설정이 우선(본문 우선). 단 이건 '참조 자료'지 본문이 아니다 —
//  설정 문장을 그대로 옮기거나 구역·장소를 나열·해설하지 말고(구조도를 읊는 사고),
//  장면이 놓인 한 자리만 보여주며 필요할 때 자연스럽게 녹여 쓰도록 지시한다.
// ─────────────────────────────────────────────────────────────────────────

export function buildLoreContext(entries) {
  const active = (entries || []).filter((e) => e && e.is_active !== false && e.title);
  if (active.length === 0) return '';

  const 항목 = active.map((e) => {
    const 머리 = e.category ? `■ ${e.title} (${e.category})` : `■ ${e.title}`;
    return e.body ? `${머리}\n${e.body}` : 머리;
  });

  return (
    '[연재 고유 설정]\n' +
    '아래는 이 연재가 원작에서 바꾸거나 새로 정한 세계 설정이다. ' +
    '원작 기본값과 갈리는 지점에서는 이 설정을 기준으로 삼는다. ' +
    '이건 사실을 틀리지 않기 위한 참조 자료일 뿐, 본문에 풀어 설명할 목록이 아니다 — ' +
    '설정의 문장을 그대로 옮기거나, 구역·장소·항목을 줄줄이 나열하지 말 것. ' +
    '특히 지리·구조(궁의 권역, 건물 배치 등)는 장면이 놓인 그 한 자리만 구체적 감각으로 보여주고, ' +
    '다른 장소를 훑거나 "어디는 무엇이 있는 곳"이라고 정의·해설하지 않는다(지도 범례처럼 읽힌다). ' +
    '그 장면에 필요할 때 서술 속에 자연스럽게 녹여 쓸 것.\n\n' +
    항목.join('\n\n')
  );
}
